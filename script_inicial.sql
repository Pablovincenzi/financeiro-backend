            SELECT DISTINCT cd.id, d.usuario_id, CURRENT_TIMESTAMP
            FROM despesas d
            JOIN categorias_despesa cd
                ON cd.nome = COALESCE(NULLIF(TRIM(d.categoria), ''), 'Sem categoria')
            WHERE NOT EXISTS (
                SELECT 1
                FROM categorias_despesa_usuarios cdu
